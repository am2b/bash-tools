main() {
    REQUIRED_TOOLS=()
    check_dependent_tools "${REQUIRED_TOOLS[@]}"
    REQUIRED_ENVS=()
    check_envs "${REQUIRED_ENVS[@]}" || exit 1
    process_opts "${@}"
    shift $((OPTIND - 1))
    check_parameters "${@}"

    local lan
    lan=$(echo "${1}" | tr '[:upper:]' '[:lower:]')

    case "${lan}" in
        go)
            for_go
            ;;
        python)
            for_python
            ;;
        *)
            echo "不支持该语言"
            ;;
    esac
}

main "${@}"
